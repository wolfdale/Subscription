package com.api.customer;

public class userInfoJSON {
	String customer_id;
	String customer_name;
	String customer_age;
	String customer_sub;
	public String getCustomer_id() {
		return customer_id;
	}
	public void setCustomer_id(String customer_id) {
		this.customer_id = customer_id;
	}
	public String getCustomer_name() {
		return customer_name;
	}
	public void setCustomer_name(String customer_name) {
		this.customer_name = customer_name;
	}
	public String getCustomer_age() {
		return customer_age;
	}
	public void setCustomer_age(String customer_age) {
		this.customer_age = customer_age;
	}
	public String getCustomer_sub() {
		return customer_sub;
	}
	public void setCustomer_sub(String customer_sub) {
		this.customer_sub = customer_sub;
	}

}
